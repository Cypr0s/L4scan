/**
 * @def main
 * @brief entry and exit point of progam
 */
int main(int argc, char** argv) {
    Scanner scanner = {0};

    // https://en.wikipedia.org/wiki/C_signal_handling setup handling signals
    if (signal(SIGINT, catch_function) == SIG_ERR) {
        fprintf(stderr, "An error occurred while setting a signal handler.\n");
        return ERR_FAILURE;
    }

    if (signal(SIGTERM, catch_function) == SIG_ERR) {
        fprintf(stderr, "An error occurred while setting a signal handler.\n");
        return ERR_FAILURE;
    }

	// handle input arguments
    if(parse_arguments(argc, argv, &scanner)) {
        return ERR_INVALID_ARGUMENT;
    }

	// help message flag, return with exit code 0
    if(scanner.parameter_flags & HELP_FLG) {
        return ERR_SUCCESS;
    }

    // handle -i parameter
	// print interfaces and return
    if(scanner.interface_name == NULL) {
        return print_interfaces();
    }

    // handle hostname (get ips from hostname), store them in addrinfo structs, 
	// store types of ip versions in scanner which will be further used for sockets, ..
    struct addrinfo* addresses;
    ExitEnum err = get_addresses_from_hostname(&addresses, &scanner);
    if(err) {
        return err;
    }

    // get the FIRST address of ipv4 interface or/and ipv6 global interface
    err = get_interfaces(&scanner);
    if(err) {
        freeaddrinfo(addresses);
        return err;
    }

    // create sockets, bind them to correct interfaces or set options for ipv6 ones
	Sockets socks;
    init_sockets_struct(&socks); // init to -1 all

	if(create_sockets(&socks, &scanner)) { // actually create needed sockets
		freeaddrinfo(addresses);
		destroy_sockets(&socks);
		return ERR_SOCKET;
	}

    // set correct socket options and bind sockets
    if(bind_sockets(&socks, &scanner)) {
		freeaddrinfo(addresses);
		destroy_sockets(&socks);
		return ERR_SOCKET;
	}

    // do the actual L4 scanning
    err = scan_ip_addresses(&scanner, addresses, &socks);
    if(err) {
        return err;
    }

	destroy_sockets(&socks); // free sockets
	freeaddrinfo(addresses); // free addresses
    return ERR_SUCCESS;
} // main